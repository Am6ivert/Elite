(function () {
  var DEF = {};
  window.EA_TEXT_SCHEMA.forEach(function (g) {
    g.items.forEach(function (it) { DEF[it.k] = it.d; });
  });
  window.EA_TEXT_DEFAULTS = DEF;
  /* eaText(key) → админ-правка, иначе стандартный текст */
  window.eaText = function (k) {
    var o = (window.eaContent && window.eaContent("texts", null)) || {};
    var v = o[k];
    if (v !== undefined && v !== null && String(v).trim() !== "") return v;
    return DEF[k] !== undefined ? DEF[k] : k;
  };
  /* Многострочный текст → массив строк */
  window.eaTextLines = function (k) { return String(window.eaText(k)).split("\n"); };
  /* Многострочный текст → React-узлы с <br/> между строками */
  window.eaTextBr = function (k) {
    var lines = window.eaTextLines(k);
    return lines.map(function (s, i) {
      return React.createElement(React.Fragment, { key: i }, s, i < lines.length - 1 ? React.createElement("br") : null);
    });
  };
})();
